package com.harman.batch4;

public class ParentCalculator {
    public int AdditionOperation(int x, int y)
    {
        return x+y;
    }

    public int SubtractOperation(int x, int y)
    {
        return x-y;
    }

    public int MultiplicationOperation(int x, int y)
    {
        return x*y;
    }

}
